class Relation{

	public static void main(String arg[])
	{
	int a=15;
	int b=16;
	System.out.println(a);
	System.out.println(b);
	System.out.println("using a equals to b " + (a==b));	
	System.out.println("using a not equals to b " + (a!=b));	
	System.out.println("using a less than b "  + (a<b));
	System.out.println("using a less than equals to b " + (a<=b));
	System.out.println("using a greater than b " + (a>b));
	System.out.println("using a greater than equals to b " + (a>=b));	
	}
}
  
